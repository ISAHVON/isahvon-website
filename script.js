const html = document.documentElement;
const languageSwitch = document.querySelector(".language-switch");
const languageLabels = document.querySelectorAll("[data-lang-label]");
const navToggle = document.querySelector(".nav-toggle");
const navMenu = document.querySelector(".nav-menu");
const form = document.querySelector("#inquiry-form");
const revealItems = document.querySelectorAll(".reveal");

let currentLanguage = localStorage.getItem("isahvon-language") || "sv";

function applyLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem("isahvon-language", lang);
  html.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = translations[lang][key];
    if (!value) return;
    element.textContent = value;
  });

  languageLabels.forEach((label) => {
    label.classList.toggle("is-active", label.dataset.langLabel === lang);
  });

  const locationInput = document.querySelector("#location");
  const messageInput = document.querySelector("#message");

  if (locationInput) {
    locationInput.placeholder = lang === "sv" ? "Karlstad, Stockholm, Ibiza..." : "Karlstad, Stockholm, Ibiza...";
  }

  if (messageInput) {
    messageInput.placeholder =
      lang === "sv"
        ? "Berätta kort om känslan, gästerna och vad ni vill skapa."
        : "Tell us briefly about the vibe, guests and what you want to create.";
  }
}

function toggleMenu() {
  const isOpen = navMenu.classList.toggle("is-open");
  navToggle.classList.toggle("is-open", isOpen);
  navToggle.setAttribute("aria-expanded", String(isOpen));
  document.body.classList.toggle("menu-open", isOpen);
}

function closeMenu() {
  navMenu.classList.remove("is-open");
  navToggle.classList.remove("is-open");
  navToggle.setAttribute("aria-expanded", "false");
  document.body.classList.remove("menu-open");
}

function getFormValue(name) {
  const field = form.elements[name];
  return field ? field.value.trim() : "";
}

function handleInquirySubmit(event) {
  event.preventDefault();

  if (!form.reportValidity()) return;

  const t = translations[currentLanguage];
  const bodyLines = [
    t.emailBodyTitle,
    "",
    `${t.emailName}: ${getFormValue("name")}`,
    `Email: ${getFormValue("email")}`,
    `${t.emailPhone}: ${getFormValue("phone") || "-"}`,
    `${t.emailDate}: ${getFormValue("date") || "-"}`,
    `${t.emailLocation}: ${getFormValue("location") || "-"}`,
    `${t.emailType}: ${getFormValue("eventType") || "-"}`,
    `${t.emailBudget}: ${getFormValue("budget") || "-"}`,
    "",
    `${t.emailMessage}:`,
    getFormValue("message") || "-"
  ];

  const mailto = new URL("mailto:Isahvon@outlook.com");
  mailto.searchParams.set("subject", t.emailSubject);
  mailto.searchParams.set("body", bodyLines.join("\n"));
  window.location.href = mailto.toString();
}

function setupRevealAnimation() {
  if (!("IntersectionObserver" in window)) {
    revealItems.forEach((item) => item.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16 }
  );

  revealItems.forEach((item) => observer.observe(item));
}

languageSwitch.addEventListener("click", () => {
  applyLanguage(currentLanguage === "sv" ? "en" : "sv");
});

navToggle.addEventListener("click", toggleMenu);
navMenu.querySelectorAll("a").forEach((link) => link.addEventListener("click", closeMenu));
form.addEventListener("submit", handleInquirySubmit);

applyLanguage(currentLanguage);
setupRevealAnimation();
